export const getCategoryColor = (category: string) => {
  switch (category.toLowerCase()) {
    case 'politics':
      return designSystem.colors.politics;
    case 'technology':
      return designSystem.colors.technology;
    case 'business':
      return designSystem.colors.business;
    case 'finance':
      return designSystem.colors.finance;
    case 'investing.com':
    case 'investing':
      return designSystem.colors.investing;
    default:
      return designSystem.colors.secondary;
  }
};

export const getPublisherColor = (publisher: string) => {
  switch (publisher.toLowerCase()) {
    case 'weekly voice':
      return designSystem.colors.politics;
    case 'investing.com':
      return designSystem.colors.investing;
    case 'cnhi news':
      return designSystem.colors.business;
    case 'tech daily':
      return designSystem.colors.technology;
    case 'finance weekly':
      return designSystem.colors.finance;
    default:
      return designSystem.colors.primary;
  }
};